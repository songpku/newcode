/*
矩形覆盖

题目描述
我们可以用2*1的小矩形横着或者竖着去覆盖更大的矩形。请问用n个2*1的小矩形无重叠地覆盖一个2*n的大矩形，总共有多少种方法？

*/ 
class Solution {
public:
    int rectCover(int number) {
		if(number == 0)
            return 0;
        int fn_2=0;
        int fn_1=1;
        int ret;
        for(int i=1;i<=number;i++){
            ret = fn_1 + fn_2;
            fn_2 = fn_1;
            fn_1 = ret;
        }
        return ret;
    }
};
